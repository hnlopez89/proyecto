<template>
  <div>
    <input v-model="recoverCode" type="text" placeholder="Escribe tu c칩digo de recuperaci칩n" />
    <br />
    <input v-model="newPassword" type="password" placeholder="Escribe tu nueva contrase침a" />
    <button @click="resetPassword()">Cambia tu contrase침a</button>
  </div>
</template>

<script>
import axios from "axios";

export default {
  name: "ResetUserPassword",
  data() {
    return {
      recoverCode: "",
      newPassword: "",
    };
  },
  methods: {
    async resetPassword() {
      try {
        const response = await axios.post(
          "http://localhost:3000/user/reset-password",
          {
            recoverCode: this.recoverCode,
            newPassword: this.newPassword,
          }
        );
        console.log(response);
      } catch (error) {
        console.log(error.response.data);
      }
    },
  },
};
</script>

